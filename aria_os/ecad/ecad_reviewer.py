# -*- coding: utf-8 -*-
"""
ecad_reviewer.py - ECAD layout and component reviewer for KiCad pcbnew scripts.

Analyzes an existing *_pcbnew.py script (generated by ecad_generator.py), suggests
layout/component improvements via LLM (or heuristic fallback), applies selected edits,
and writes a revised script alongside the original.

Usage:
    from aria_os.ecad.ecad_reviewer import review_ecad
    out = review_ecad("outputs/ecad/aria_esp32_board/aria_esp32_board_pcbnew.py")

    # With hint and non-interactive (apply all suggestions automatically):
    out = review_ecad(script_path, hint="add I2C header", interactive=False)
"""
from __future__ import annotations

import json
import re
import sys
from pathlib import Path
from typing import Any

# LLM client (graceful import)
try:
    from aria_os.llm_client import call_llm  # type: ignore
    _HAS_LLM = True
except ImportError:
    _HAS_LLM = False

    def call_llm(prompt: str, system: str, *, repo_root=None) -> "str | None":  # type: ignore
        return None


# =============================================================================
# Step 1 - Analysis
# =============================================================================

def _analyse_script(script_text: str, bom: "dict | None") -> dict:
    """
    Parse a pcbnew Python script (and optional BOM dict) to extract a structured
    analysis dictionary.  Pure regex -- the script is never executed.
    """
    analysis: dict[str, Any] = {
        "board_dims": None,
        "components": [],
        "component_count": 0,
        "has_esp32": False,
        "has_stm32": False,
        "has_power_connector": False,
        "has_uart": False,
        "has_i2c": False,
        "has_spi": False,
        "has_usb": False,
        "decoupling_caps_count": 0,
        "has_fuse": False,
        "has_tvs": False,
        "firmware_pins": [],
    }

    # Board dimensions: BOARD_W_MM = 80.0 / BOARD_H_MM = 60.0
    w_m = re.search(r"BOARD_W_MM\s*=\s*([\d.]+)", script_text)
    h_m = re.search(r"BOARD_H_MM\s*=\s*([\d.]+)", script_text)
    if w_m and h_m:
        analysis["board_dims"] = f"{w_m.group(1)}x{h_m.group(1)}mm"
    else:
        # Fallback: SetBoardSize or mm(80), mm(60) patterns
        bsize = re.search(r"SetBoardSize.*?mm\((\d+)\).*?mm\((\d+)\)", script_text)
        if bsize:
            analysis["board_dims"] = f"{bsize.group(1)}x{bsize.group(2)}mm"

    # Component refs from add_fp calls:  add_fp(board, "U1", "ESP32-S3-WROOM-1", ...)
    refs: list[str] = re.findall(r'add_fp\s*\([^,]+,\s*"([A-Z]+\d+)"', script_text)
    # Also catch direct footprint adds: fp.SetReference("C3")
    refs += re.findall(r'SetReference\s*\(\s*"([A-Z]+\d+)"\s*\)', script_text)
    # Deduplicate preserving order
    seen: set[str] = set()
    unique_refs: list[str] = []
    for r in refs:
        if r not in seen:
            seen.add(r)
            unique_refs.append(r)

    # Supplement with BOM if present
    if bom and "components" in bom:
        for comp in bom["components"]:
            r = comp.get("ref", "")
            if r and r not in seen:
                seen.add(r)
                unique_refs.append(r)

    analysis["components"] = unique_refs
    analysis["component_count"] = len(unique_refs)

    # MCU detection
    lower = script_text.lower()
    analysis["has_esp32"] = bool(re.search(r"esp32", lower))
    analysis["has_stm32"] = bool(re.search(r"stm32", lower))

    # Interface detection
    analysis["has_power_connector"] = bool(
        re.search(r"barrel.?jack|vin|12v|24v|power.?connector", lower)
    )
    analysis["has_uart"] = bool(re.search(r"uart|tx.*rx|serial", lower))
    analysis["has_i2c"] = bool(re.search(r"i2c|sda|scl", lower))
    analysis["has_spi"] = bool(re.search(r"\bspi\b|mosi|miso|sclk", lower))
    analysis["has_usb"] = bool(re.search(r"\busb\b|usb.?c|micro.?usb", lower))

    # Decoupling capacitors -- count unique C* refs; use BOM qty when available
    cap_refs = {r for r in unique_refs if r.startswith("C")}
    if bom and "components" in bom:
        total_caps = 0
        for comp in bom["components"]:
            if comp.get("ref", "").startswith("C"):
                total_caps += comp.get("qty", 1)
        analysis["decoupling_caps_count"] = total_caps
    else:
        analysis["decoupling_caps_count"] = len(cap_refs)

    # Protection components
    fuse_refs = {r for r in unique_refs if r.startswith("F")}
    analysis["has_fuse"] = (
        bool(fuse_refs) or bool(re.search(r"fuse|polyfuse|resettable", lower))
    )

    tvs_in_names = bool(
        re.search(r"\btvs\b|transient.?voltage", lower)
    )
    tvs_d_refs = {
        r for r in unique_refs
        if r.startswith("D") and re.search(r"tvs|smaj|smbj|protection", lower)
    }
    analysis["has_tvs"] = bool(tvs_d_refs) or tvs_in_names

    # Firmware pins: lines like  # PIN_BRAKE = PB13
    fw_pins = re.findall(r"#\s*(PIN_\w+|\w+_GPIO_NUM)\s*=\s*(\S+)", script_text)
    analysis["firmware_pins"] = [f"{k}={v}" for k, v in fw_pins]

    return analysis


def _analyse_bom_file(bom_path: Path) -> "dict | None":
    """Load BOM JSON if present; return None on any error."""
    if not bom_path.exists():
        return None
    try:
        return json.loads(bom_path.read_text(encoding="utf-8"))
    except Exception:
        return None


