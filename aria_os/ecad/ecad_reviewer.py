_SYSTEM_PROMPT = (
    "You are an experienced PCB designer and embedded systems engineer reviewing a "
    "KiCad board layout script.  Provide specific, actionable suggestions as a JSON array."
)


def _build_llm_prompt(analysis: dict, hint: str, script_lines: list[str]) -> str:
    preview = "\n".join(script_lines[:100])
    schema_example = (
        '[\n'
        '  {\n'
        '    "id": <int>,\n'
        '    "description": "<specific, actionable description>",\n'
        '    "priority": "required" | "recommended" | "optional",\n'
        '    "edit_type": "add_component" | "set_trace_width" | "add_comment"'
        ' | "add_net_tie" | "rename_ref" | "add_keepout",\n'
        '    "component": {"ref": "...", "value": "...", "footprint": "...", "description": "..."},\n'
        '    "position_hint": "...",\n'
        '    "net": "...",\n'
        '    "width_mm": <float>\n'
        '  }\n'
        ']'
    )
    return (
        "Review the following KiCad pcbnew script and return ONLY a JSON array of improvement "
        "suggestions.  Each suggestion must follow this exact schema:\n"
        + schema_example + "\n\n"
        + f"Board analysis:\n{json.dumps(analysis, indent=2)}\n\n"
        + (f"User hint: {hint}\n\n" if hint else "")
        + f"First 100 lines of script:\n```python\n{preview}\n```\n\n"
        + "Return ONLY the JSON array -- no other text."
    )


def _heuristic_suggestions(analysis: dict) -> list[dict]:
    """
    Generate basic suggestions from the analysis dict when no LLM is available.
    """
    suggestions: list[dict] = []
    sid = 1

    # Decoupling caps: ESP32 needs >=4, STM32 needs >=6
    min_caps = 0
    if analysis["has_esp32"]:
        min_caps += 4
    if analysis["has_stm32"]:
        min_caps += 6
    found_caps = analysis["decoupling_caps_count"]
    if min_caps > 0 and found_caps < min_caps:
        missing = min_caps - found_caps
        who = []
        if analysis["has_esp32"]:
            who.append("ESP32")
        if analysis["has_stm32"]:
            who.append("STM32")
        who_str = "/".join(who)
        suggestions.append({
            "id": sid,
            "description": (
                f"Add {missing} more 100nF decoupling capacitor(s) for {who_str} "
                f"power pins -- only {found_caps} found, need {min_caps} minimum"
            ),
            "priority": "required",
            "edit_type": "add_component",
            "component": {
                "ref": f"C{found_caps + 1}",
                "value": "100nF",
                "footprint": "Capacitor_SMD:C_0402_1005Metric",
                "description": "Decoupling cap VDD",
            },
            "position_hint": f"Place near {who_str} VDD pins, within 0.5mm",
        })
        sid += 1

    # Fuse on power input
    if analysis["has_power_connector"] and not analysis["has_fuse"]:
        suggestions.append({
            "id": sid,
            "description": (
                "Add 500mA polyfuse on VIN before LDO -- no overcurrent protection found"
            ),
            "priority": "required",
            "edit_type": "add_component",
            "component": {
                "ref": "F1",
                "value": "500mA polyfuse",
                "footprint": "Fuse:Fuse_1812_4532Metric",
                "description": "Overcurrent protection on VIN",
            },
            "position_hint": "Place on VIN net before LDO input",
        })
        sid += 1

    # TVS on power input
    if analysis["has_power_connector"] and not analysis["has_tvs"]:
        suggestions.append({
            "id": sid,
            "description": (
                "Add TVS diode on VIN for ESD/transient protection -- "
                "recommended for external power connections"
            ),
            "priority": "recommended",
            "edit_type": "add_component",
            "component": {
                "ref": "D2",
                "value": "SMAJ15A",
                "footprint": "Diode_SMD:D_SMA",
                "description": "TVS diode 15V clamping on VIN",
            },
            "position_hint": "Place between power connector and fuse on VIN",
        })
        sid += 1

    # I2C header missing but ESP32 present
    if analysis["has_esp32"] and not analysis["has_i2c"]:
        suggestions.append({
            "id": sid,
            "description": (
                "Add I2C header (SDA/SCL/3V3/GND) -- ESP32 has I2C capability "
                "but no I2C connector was found in the script"
            ),
            "priority": "recommended",
            "edit_type": "add_component",
            "component": {
                "ref": "J5",
                "value": "JST-XH-4P-I2C",
                "footprint": "Connector_JST:JST_XH_B4B-XH-A_1x04_P2.50mm_Vertical",
                "description": "I2C expansion header (SDA/SCL/3V3/GND)",
            },
            "position_hint": "Place on board edge for easy access",
        })
        sid += 1

    # VIN trace width reminder when power connector present
    if analysis["has_power_connector"]:
        suggestions.append({
            "id": sid,
            "description": (
                "Verify VIN trace width -- for 12V/500mA+ input, trace must be >=1.0mm. "
                "Default 0.25mm traces will violate IPC-2221 thermal limits."
            ),
            "priority": "recommended",
            "edit_type": "set_trace_width",
            "net": "VIN",
            "width_mm": 1.0,
        })
        sid += 1

    return suggestions


def _call_llm_suggestions(
    analysis: dict,
    hint: str,
    script_lines: list[str],
    repo_root: "Path | None",
) -> "list[dict] | None":
    """
    Call LLM and parse JSON array from response.
    Returns None if LLM is unavailable or returns unparseable output.
    """
    if not _HAS_LLM:
        return None
    prompt = _build_llm_prompt(analysis, hint, script_lines)
    raw = call_llm(prompt, _SYSTEM_PROMPT, repo_root=repo_root)
    if not raw:
        return None
    # Extract JSON array from response (LLM may wrap in prose)
    m = re.search(r"\[.*\]", raw, re.DOTALL)
    if not m:
        return None
    try:
        parsed = json.loads(m.group(0))
        if isinstance(parsed, list):
            return parsed
    except json.JSONDecodeError:
        pass
    return None


