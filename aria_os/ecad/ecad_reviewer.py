def review_ecad(
    script_path: "str | Path",
    hint: str = "",
    repo_root: "Path | None" = None,
    interactive: bool = True,
) -> Path:
    """
    Analyze a KiCad pcbnew Python script, suggest improvements, apply selected
    edits, and write a revised script alongside the original.

    Parameters
    ----------
    script_path:
        Path to *_pcbnew.py (or *_bom.json -- will resolve to the .py sibling).
    hint:
        Optional user guidance, e.g. "add I2C header" or "increase trace width".
    repo_root:
        Repository root passed through to call_llm for key resolution.
    interactive:
        When True (default) prompt user to select suggestions.
        When False, apply all required + recommended automatically.

    Returns
    -------
    Path to the revised script (<stem>_revised.py in the same directory).
    """
    script_path = Path(script_path)

    # Allow passing a BOM JSON path -- resolve to .py sibling
    if script_path.suffix == ".json":
        py_path = script_path.parent / script_path.name.replace("_bom.json", "_pcbnew.py")
        script_path = py_path

    if not script_path.exists():
        raise FileNotFoundError(f"[ECAD REVIEW] Script not found: {script_path}")

    script_text = script_path.read_text(encoding="utf-8")
    script_lines = script_text.splitlines()

    # Derive BOM path
    bom_path = script_path.parent / script_path.name.replace("_pcbnew.py", "_bom.json")
    bom = _analyse_bom_file(bom_path)

    # Step 1: Analyse
    analysis = _analyse_script(script_text, bom)
    analysis["file"] = script_path.name

    # Step 2: Generate suggestions
    suggestions = _call_llm_suggestions(analysis, hint, script_lines, repo_root)
    if not suggestions:
        suggestions = _heuristic_suggestions(analysis)

    # Ensure IDs are sequential integers
    for i, s in enumerate(suggestions, start=1):
        s["id"] = i

    if not suggestions:
        print(f"[ECAD REVIEW] No suggestions generated for {script_path.name}.")
        # Always return a valid path
        out_path = script_path.parent / f"{script_path.stem}_revised.py"
        out_path.write_text(script_text, encoding="utf-8")
        return out_path

    # Step 3: Display and select
    _display_suggestions(suggestions, analysis, script_path.name)
    selected = _prompt_selection(suggestions, interactive)

    if not selected:
        print("[ECAD REVIEW] No edits selected -- writing unmodified revised script.")
        out_path = script_path.parent / f"{script_path.stem}_revised.py"
        out_path.write_text(script_text, encoding="utf-8")
        _write_review_sidecar(script_path.parent, script_path.stem, analysis, suggestions, [])
        return out_path

    # Step 4: Apply
    revised_text, applied = _apply_suggestions(script_text, selected)

    # Step 5: Save
    out_path = script_path.parent / f"{script_path.stem}_revised.py"
    out_path.write_text(revised_text, encoding="utf-8")

    _write_review_sidecar(
        script_path.parent, script_path.stem, analysis, suggestions, applied
    )

    # Friendly display path relative to cwd when possible
    try:
        display = out_path.relative_to(Path.cwd())
    except ValueError:
        display = out_path

    print(
        f"[ECAD REVIEW] Applied {len(applied)} edit(s). "
        f"Revised script: {display}"
    )
    print(
        "[ECAD REVIEW] Run revised script in KiCad scripting console to apply to your board."
    )

    return out_path


