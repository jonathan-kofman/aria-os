if __name__ == "__main__":
    import argparse

    parser = argparse.ArgumentParser(
        description="Review and improve a KiCad pcbnew Python script."
    )
    parser.add_argument("script", help="Path to *_pcbnew.py or *_bom.json")
    parser.add_argument("--hint", default="", help="Optional guidance for the reviewer")
    parser.add_argument(
        "--no-interactive",
        action="store_true",
        help="Apply all required + recommended suggestions automatically",
    )
    args = parser.parse_args()

    result = review_ecad(
        args.script,
        hint=args.hint,
        interactive=not args.no_interactive,
    )
    print(f"Output: {result}")
