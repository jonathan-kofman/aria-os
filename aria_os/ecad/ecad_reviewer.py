_PRIORITY_LABEL = {
    "required": "REQUIRED    ",
    "recommended": "RECOMMENDED ",
    "optional": "OPTIONAL    ",
}


def _display_suggestions(suggestions: list[dict], analysis: dict, file_name: str) -> None:
    dims = analysis.get("board_dims") or "unknown dims"
    parts: list[str] = []
    if analysis["has_esp32"]:
        parts.append("ESP32")
    if analysis["has_stm32"]:
        parts.append("STM32")
    board_desc = " + ".join(parts) if parts else "board"
    n_comp = analysis["component_count"]

    print(f"\n[ECAD REVIEW] {len(suggestions)} suggestion(s) for {file_name}:")
    print(f"  Board: {board_desc}, {dims}, {n_comp} components found")
    for s in suggestions:
        label = _PRIORITY_LABEL.get(s.get("priority", "optional"), "          ")
        print(f"  [{s['id']}] {label} {s['description']}")


def _prompt_selection(suggestions: list[dict], interactive: bool) -> list[dict]:
    """
    Ask user which suggestions to apply.  Returns filtered list.
    If interactive=False, apply all required + recommended automatically.
    """
    if not interactive:
        selected = [s for s in suggestions if s.get("priority") in ("required", "recommended")]
        if not selected:
            selected = suggestions
        return selected

    required = [s for s in suggestions if s.get("priority") == "required"]
    default_ids = {s["id"] for s in required} if required else {s["id"] for s in suggestions}
    default_label = "all required" if required else "all"

    print(
        f"\nApply which? [all / comma-separated ids e.g. 1,2,3 / none] "
        f"(default: {default_label}): ",
        end="",
        flush=True,
    )
    try:
        raw = sys.stdin.readline().strip()
    except (EOFError, OSError):
        raw = ""

    if not raw:
        return [s for s in suggestions if s["id"] in default_ids]
    if raw.lower() == "all":
        return suggestions
    if raw.lower() == "none":
        return []
    try:
        ids = {int(x.strip()) for x in raw.split(",") if x.strip()}
        return [s for s in suggestions if s["id"] in ids]
    except ValueError:
        print("[ECAD REVIEW] Invalid input -- applying default (all required).")
        return [s for s in suggestions if s["id"] in default_ids]


# =============================================================================
# Step 4 - Apply edits
# =============================================================================

def _find_last_component_position(script_text: str) -> "tuple[float, float]":
    """
    Scan existing add_fp calls to find the last x,y position used.
    Returns (x, y) in mm; defaults to (5.0, 45.0) if none found.
    """
    # Pattern: add_fp(board, "REF", "VALUE", "FOOTPRINT", x, y, ...)
    matches = re.findall(
        r'add_fp\s*\([^,]+,\s*"[^"]+",\s*"[^"]+",\s*"[^"]+",\s*([\d.]+),\s*([\d.]+)',
        script_text,
    )
    if matches:
        last = matches[-1]
        return float(last[0]), float(last[1])
    return 5.0, 45.0


def _apply_add_component(
    script_text: str,
    suggestion: dict,
    x: float,
    y: float,
) -> str:
    comp = suggestion.get("component", {})
    ref = comp.get("ref", "C99")
    value = comp.get("value", "unknown")
    footprint = comp.get("footprint", "")
    description = comp.get("description", suggestion.get("description", ""))
    position_hint = suggestion.get("position_hint", "")

    block = (
        f"\n# {description}\n"
        f"# Position hint: {position_hint}\n"
        f"add_fp(board, \"{ref}\", \"{value}\", \"{footprint}\","
        f" {x:.2f}, {y:.2f}, \"{description}\")\n"
    )

    # Insert before pcbnew.Refresh() / board.Save() if present, else append
    for marker in ("pcbnew.Refresh()", "board.Save("):
        idx = script_text.find(marker)
        if idx != -1:
            return script_text[:idx] + block + script_text[idx:]

    return script_text + block


def _apply_set_trace_width(script_text: str, suggestion: dict) -> str:
    net = suggestion.get("net", "VIN")
    width_mm = suggestion.get("width_mm", 1.0)

    block = (
        f"\n# Set trace width for net {net} to {width_mm}mm\n"
        f"for track in board.GetTracks():\n"
        f"    if track.GetNetname() == \"{net}\":\n"
        f"        track.SetWidth(pcbnew.FromMM({width_mm}))\n"
    )

    for marker in ("pcbnew.Refresh()", "board.Save("):
        idx = script_text.find(marker)
        if idx != -1:
            return script_text[:idx] + block + script_text[idx:]

    return script_text + block


def _apply_add_comment(script_text: str, suggestion: dict) -> str:
    description = suggestion.get("description", "")
    block = f"\n# REVIEW NOTE: {description}\n"
    return script_text + block


def _apply_add_keepout(script_text: str, suggestion: dict) -> str:
    description = suggestion.get("description", "")
    block = (
        f"\n# KEEPOUT: {description}\n"
        "# TODO: Add keepout zone manually in KiCad PCB editor (Place > Rule Area).\n"
    )
    return script_text + block


def _apply_suggestions(script_text: str, selected: list[dict]) -> "tuple[str, list[dict]]":
    """
    Apply a list of selected suggestions to the script text.
    Returns (revised_text, list_of_applied_suggestions).
    """
    revised = script_text
    applied: list[dict] = []

    # Start new component placements just after the last known component
    last_x, last_y = _find_last_component_position(script_text)
    stagger = 0

    for suggestion in selected:
        edit_type = suggestion.get("edit_type", "add_comment")
        try:
            if edit_type == "add_component":
                nx = last_x
                ny = last_y + 5.0 + stagger * 5.0
                stagger += 1
                revised = _apply_add_component(revised, suggestion, nx, ny)
                applied.append(suggestion)

            elif edit_type == "set_trace_width":
                revised = _apply_set_trace_width(revised, suggestion)
                applied.append(suggestion)

            elif edit_type == "add_comment":
                revised = _apply_add_comment(revised, suggestion)
                applied.append(suggestion)

            elif edit_type in ("add_net_tie", "rename_ref", "add_keepout"):
                revised = _apply_add_keepout(revised, suggestion)
                applied.append(suggestion)

            else:
                # Unknown edit type -- add as comment
                revised = _apply_add_comment(revised, suggestion)
                applied.append(suggestion)

        except Exception as exc:
            print(f"[ECAD REVIEW] Warning: could not apply edit {suggestion.get('id')}: {exc}")

    return revised, applied


# =============================================================================
# Step 5 - Save
# =============================================================================

def _write_review_sidecar(
    out_dir: Path,
    stem: str,
    analysis: dict,
    suggestions: list[dict],
    applied: list[dict],
) -> Path:
    sidecar = out_dir / f"{stem}_revised_review.json"
    data = {
        "analysis": analysis,
        "suggestions": suggestions,
        "applied": [s["id"] for s in applied],
        "applied_count": len(applied),
    }
    sidecar.write_text(json.dumps(data, indent=2), encoding="utf-8")
    return sidecar


