"""
aria_cert_package.py — Certification Evidence Package Generator
===============================================================
One-click export of everything needed for an ANSI Z359.14 pre-submission
or engineering design review. Produces a dated ZIP containing:

  ARIA_Cert_Package_YYYYMMDD/
  ├── 00_Cover_Page.txt
  ├── 01_CEM_Design_Report.pdf
  ├── 02_ANSI_Compliance_Summary.txt
  ├── 03_Drop_Test_Results/
  ├── 04_Static_Load_Results/
  ├── 05_State_Machine_Validation.txt
  ├── 06_Constants_Sync_Report.txt
  ├── 07_Design_History.json
  ├── 08_HIL_Test_Report.json
  └── 09_Open_Items.txt

Add to aria_dashboard.py:
    from aria_cert_package import render_cert_package_tab

SETUPS entry:
    "Certification Package": ["Generate evidence package"],

Routing:
    elif setup.startswith("Certification Package"):
        render_cert_package_tab()
"""

import io
import json
import os
import zipfile
from datetime import datetime
from pathlib import Path

ANSI_LIMITS = {
    'peak_force_N':       6000.0,
    'arrest_distance_mm': 1000.0,
    'avg_force_N':        4000.0,
    'static_sf_min':      2.0,
    'clutch_margin_min':  3.0,
}


def _load_json(path: Path):
    try:
        return json.loads(path.read_text())
    except Exception:
        return None


def _collect_drop_results() -> list:
    results = []
    d = Path("drop_test_results")
    if d.exists():
        for f in sorted(d.glob("drop_test_*.json")):
            data = _load_json(f)
            if data:
                results.append({'file': f.name, 'data': data})
    return results


def _collect_design_history() -> list:
    h = Path("cem_design_history.json")
    if h.exists():
        data = _load_json(h)
        return data if isinstance(data, list) else []
    return []


def _collect_hil_results():
    candidates = list(Path(".").glob("hil_report_*.json"))
    if Path("tools").exists():
        candidates += list(Path("tools").glob("hil_report_*.json"))
    candidates = sorted(candidates)
    if candidates:
        return _load_json(candidates[-1])
    return None


