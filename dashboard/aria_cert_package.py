def _run_constants_sync_text() -> str:
    import subprocess, sys
    try:
        result = subprocess.run(
            [sys.executable, "tools/aria_constants_sync.py", "--ci", "--repo-root", "."],
            capture_output=True, text=True, timeout=10
        )
        return result.stdout or result.stderr or "Constants sync completed (no output)"
    except FileNotFoundError:
        return ("aria_constants_sync.py not found in tools/\n"
                "Copy tools/aria_constants_sync.py to your repo and run:\n"
                "  python tools/aria_constants_sync.py --verbose")
    except Exception as e:
        return f"Could not run constants sync: {e}"


def build_cert_package(
    author: str,
    purpose: str,
    device_id: str,
    geom=None,
    inputs=None,
) -> bytes:
    """
    Build the full certification evidence package as a ZIP file in memory.
    Returns bytes suitable for st.download_button.
    """
    buf = io.BytesIO()
    pkg = f"ARIA_Cert_Package_{datetime.now().strftime('%Y%m%d')}"

    with zipfile.ZipFile(buf, 'w', zipfile.ZIP_DEFLATED) as zf:

        # 00 Cover page
        zf.writestr(f"{pkg}/00_Cover_Page.txt",
                    _generate_cover_page(author, purpose, device_id))

        # 01 CEM PDF
        pdf_candidates = sorted(Path(".").glob("ARIA_CEM_Report_*.pdf"))
        if pdf_candidates:
            zf.write(str(pdf_candidates[-1]),
                     f"{pkg}/01_CEM_Design_Report.pdf")
        else:
            zf.writestr(
                f"{pkg}/01_CEM_Design_Report_NOT_GENERATED.txt",
                "Generate the PDF from the Report Generation tab first.\n"
                "CEM Design tab -> Regenerate -> Report Generation tab -> Generate PDF Report"
            )

        # 02 ANSI compliance summary
        drop_results = _collect_drop_results()
        zf.writestr(f"{pkg}/02_ANSI_Compliance_Summary.txt",
                    _generate_ansi_summary(geom, inputs, drop_results))

        # 03 Drop test results
        if drop_results:
            summary_lines = [
                f"DROP TEST RESULTS SUMMARY — {len(drop_results)} test(s)",
                f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}",
                "="*50, ""
            ]
            for r in drop_results:
                m = r['data'].get('metrics', {})
                summary_lines += [
                    f"File: {r['file']}",
                    f"  Peak force:      {m.get('peak_force_N', '?')} N  "
                    f"(limit {ANSI_LIMITS['peak_force_N']:.0f} N)  "
                    f"{'PASS' if m.get('ansi_peak_pass') else 'FAIL'}",
                    f"  Arrest distance: {m.get('arrest_distance_mm', '?')} mm  "
                    f"(limit {ANSI_LIMITS['arrest_distance_mm']:.0f} mm)  "
                    f"{'PASS' if m.get('ansi_dist_pass') else 'FAIL'}",
                    f"  Avg force:       {m.get('avg_force_N', '?')} N  "
                    f"(limit {ANSI_LIMITS['avg_force_N']:.0f} N)  "
                    f"{'PASS' if m.get('ansi_avg_pass') else 'FAIL'}",
                    f"  Overall:         {'PASS' if m.get('ansi_all_pass') else 'FAIL'}",
                    "",
                ]
                zf.writestr(
                    f"{pkg}/03_Drop_Test_Results/{r['file']}",
                    json.dumps(r['data'], indent=2)
                )
            zf.writestr(f"{pkg}/03_Drop_Test_Results/Drop_Test_Summary.txt",
                        "\n".join(summary_lines))
        else:
            zf.writestr(
                f"{pkg}/03_Drop_Test_Results/PENDING.txt",
                "No physical drop test data yet.\n\n"
                "After your first drop test:\n"
                "1. Save the CSV from your load cell logger\n"
                "2. Open Drop Test Parser tab in the dashboard\n"
                "3. Upload the CSV and click Parse\n"
                "4. Click Save to test history\n"
                "5. Regenerate this package\n"
            )

        # 04 Static load results
        static_path = Path("aria_static_test_report.txt")
        if static_path.exists():
            zf.write(str(static_path),
                     f"{pkg}/04_Static_Load_Results/aria_static_test_report.txt")
        else:
            zf.writestr(
                f"{pkg}/04_Static_Load_Results/PENDING.txt",
                "Run Setup 1 (Static Load Frame) in the dashboard,\n"
                "then click 'Download static test report'."
            )

        # 05 State machine validation
        history = _collect_design_history()
        zf.writestr(f"{pkg}/05_State_Machine_Validation.txt",
                    _generate_sm_validation(history))

        # 06 Constants sync report
        zf.writestr(f"{pkg}/06_Constants_Sync_Report.txt",
                    _run_constants_sync_text())

        # 07 Design history
        if history:
            zf.writestr(f"{pkg}/07_Design_History.json",
                        json.dumps(history, indent=2))
            summary = [
                f"CEM DESIGN HISTORY — {len(history)} runs",
                f"Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}",
                "="*50, ""
            ]
            for entry in history:
                out  = entry.get('data', {}).get('outputs', {})
                ansi = entry.get('data', {}).get('ansi', {})
                summary.append(
                    f"{entry['timestamp'][:19]}  {entry['label']}\n"
                    f"  Peak: {out.get('peak_force_kN', '?')} kN  "
                    f"Dist: {out.get('arrest_distance_m', '?')} m  "
                    f"ANSI: {'PASS' if ansi.get('all_pass') else 'FAIL'}\n"
                )
            zf.writestr(f"{pkg}/07_Design_History_Summary.txt",
                        "\n".join(summary))
        else:
            zf.writestr(
                f"{pkg}/07_Design_History.txt",
                "No CEM design history yet.\n"
                "Open CEM Design tab and click Regenerate to start logging."
            )

        # 08 HIL test report
        hil = _collect_hil_results()
        if hil:
            zf.writestr(f"{pkg}/08_HIL_Test_Report.json",
                        json.dumps(hil, indent=2))
        else:
            zf.writestr(
                f"{pkg}/08_HIL_Test_Report_PENDING.txt",
                "No HIL test report found. Run:\n\n"
                "  python tools/aria_hil_test.py --mock --suite full "
                "--json-out hil_report.json\n\n"
                "When hardware is available:\n"
                "  python tools/aria_hil_test.py --port COM3 --suite full "
                "--json-out hil_report.json\n"
            )

        # 09 Open items
        zf.writestr(f"{pkg}/09_Open_Items.txt",
                    _generate_open_items())

    buf.seek(0)
    return buf.read()


def render_cert_package_tab():
    """Streamlit tab for certification evidence package generation."""
    import streamlit as st

    st.markdown("## Certification Evidence Package")
    st.caption(
        "One-click export of everything needed for an ANSI Z359.14 pre-submission "
        "or engineering design review. Produces a dated ZIP with all test reports, "
        "CEM outputs, and compliance summary."
    )

    # ── Package readiness ──────────────────────────────────────────────────────
    st.markdown("### Package readiness")

    drop_results = _collect_drop_results()
    history      = _collect_design_history()
    cem_ready    = 'aria_geom' in st.session_state
    pdf_ready    = bool(sorted(Path(".").glob("ARIA_CEM_Report_*.pdf")))
    static_ready = Path("aria_static_test_report.txt").exists()
    hil_ready    = bool(sorted(Path(".").glob("hil_report_*.json")))

    checks = [
        ("CEM geometry computed",    cem_ready,
         "Open CEM Design -> Regenerate"),
        ("CEM PDF report generated", pdf_ready,
         "Open Report Generation -> Generate PDF"),
        ("Drop test data present",   len(drop_results) > 0,
         "Physical drop test -> Drop Test Parser -> Save"),
        ("Static test report",       static_ready,
         "Setup 1 -> Download static test report"),
        ("Design history logged",    len(history) > 0,
         "CEM Design -> Regenerate (auto-logged)"),
        ("HIL test report",          hil_ready,
         "python tools/aria_hil_test.py --mock --json-out hil_report.json"),
    ]

    ready_count = sum(1 for _, ok, _ in checks if ok)
    st.progress(ready_count / len(checks),
                text=f"{ready_count}/{len(checks)} items ready")

    col1, col2 = st.columns(2)
    for i, (label, ok, fix) in enumerate(checks):
        col = col1 if i % 2 == 0 else col2
        icon = "✅" if ok else "⚠️"
        if ok:
            col.markdown(f"{icon} {label}")
        else:
            col.markdown(f"{icon} {label}  \n*{fix}*")

    st.markdown("---")
    st.markdown(f"**Drop tests saved:** {len(drop_results)}  |  "
                f"**CEM runs logged:** {len(history)}")

    # ── Package metadata ───────────────────────────────────────────────────────
    st.markdown("### Package details")
    col_a, col_b = st.columns(2)
    with col_a:
        author    = st.text_input("Author", value="Jonathan Kofman")
        device_id = st.text_input("Device ID / serial", value="ARIA-POC-001")
    with col_b:
        purpose = st.selectbox("Intended audience", [
            "Internal engineering review",
            "Faculty / thesis advisor",
            "Intertek pre-certification meeting",
            "Manufacturing partner review",
            "Investor technical due diligence",
        ])

    st.markdown("---")

    # ── Generate button ────────────────────────────────────────────────────────
    if st.button("📦 Generate Certification Package", type="primary",
                 use_container_width=True):
        with st.spinner("Building package..."):
            geom      = st.session_state.get('aria_geom')
            inputs    = st.session_state.get('aria_inputs')
            pkg_bytes = build_cert_package(
                author, purpose, device_id, geom, inputs)

        fname = f"ARIA_Cert_Package_{datetime.now().strftime('%Y%m%d_%H%M')}.zip"
        st.success(f"Package ready — {len(pkg_bytes)//1024} KB")

        st.download_button(
            label=f"⬇️ Download {fname}",
            data=pkg_bytes,
            file_name=fname,
            mime="application/zip",
            use_container_width=True,
        )

        # Show contents
        with st.expander("Package contents"):
            with zipfile.ZipFile(io.BytesIO(pkg_bytes)) as zf:
                for info in sorted(zf.infolist(), key=lambda x: x.filename):
                    size_kb = info.file_size / 1024
                    st.write(f"  {info.filename}  ({size_kb:.1f} KB)")

    # ── Contents reference ─────────────────────────────────────────────────────
    with st.expander("What's in the package"):
        st.markdown("""
| File | Contents |
|------|----------|
| `00_Cover_Page.txt` | Device info, author, purpose, ANSI standard |
| `01_CEM_Design_Report.pdf` | Full physics-derived geometry report |
| `02_ANSI_Compliance_Summary.txt` | All ANSI checks with pass/fail vs limits |
| `03_Drop_Test_Results/` | All physical drop test JSONs + summary table |
| `04_Static_Load_Results/` | Static pawl/housing safety factor report |
| `05_State_Machine_Validation.txt` | State transitions verified against ANSI |
| `06_Constants_Sync_Report.txt` | Firmware vs Python model constants diff |
| `07_Design_History.json` | Every CEM run with timestamped dimensions |
| `08_HIL_Test_Report.json` | Hardware-in-the-loop test results |
| `09_Open_Items.txt` | Outstanding items before certification + assumptions |
        """)
