def _generate_ansi_summary(geom, inputs, drop_results: list) -> str:
    now = datetime.now().strftime('%Y-%m-%d %H:%M')
    lines = [
        "ANSI Z359.14 COMPLIANCE SUMMARY",
        f"Generated: {now}",
        "="*60,
        "",
        "1. ARREST FORCE (Section 4.3)",
        f"   Limit: <= {ANSI_LIMITS['peak_force_N']:.0f} N peak, "
        f"<= {ANSI_LIMITS['avg_force_N']:.0f} N average",
    ]

    if geom:
        peak = geom.predicted_peak_force_kN * 1000
        status = "PASS" if peak <= ANSI_LIMITS['peak_force_N'] else "FAIL"
        lines.append(f"   CEM prediction:  {peak:.0f} N  ->  {status}")

    if drop_results:
        for r in drop_results[-3:]:
            m = r['data'].get('metrics', {})
            if m:
                p = m.get('peak_force_N', '?')
                s = "PASS" if isinstance(p, (int, float)) and p <= ANSI_LIMITS['peak_force_N'] else "FAIL"
                lines.append(f"   Physical ({r['file']}): {p} N  ->  {s}")
    else:
        lines.append("   Physical test data: NOT YET AVAILABLE")

    lines += [
        "",
        "2. ARREST DISTANCE (Section 4.4)",
        f"   Limit: <= {ANSI_LIMITS['arrest_distance_mm']:.0f} mm",
    ]
    if geom:
        dist = geom.predicted_arrest_distance_m * 1000
        s = "PASS" if dist <= ANSI_LIMITS['arrest_distance_mm'] else "FAIL"
        lines.append(f"   CEM prediction:  {dist:.0f} mm  ->  {s}")
    if drop_results:
        for r in drop_results[-3:]:
            m = r['data'].get('metrics', {})
            d = m.get('arrest_distance_mm', '?')
            s = "PASS" if isinstance(d, (int, float)) and d <= ANSI_LIMITS['arrest_distance_mm'] else "FAIL"
            lines.append(f"   Physical ({r['file']}): {d} mm  ->  {s}")
    else:
        lines.append("   Physical test data: NOT YET AVAILABLE")

    lines += [
        "",
        "3. STRUCTURAL STRENGTH (Section 4.2)",
        f"   Minimum safety factor: >= {ANSI_LIMITS['static_sf_min']:.1f}",
    ]
    if geom:
        sf = geom.brake_drum.safety_factor
        s = "PASS" if sf >= ANSI_LIMITS['static_sf_min'] else "FAIL"
        lines.append(f"   Brake drum SF (CEM):  {sf:.2f}  ->  {s}")
        sf2 = geom.ratchet.safety_factor
        s2 = "PASS" if sf2 >= ANSI_LIMITS['static_sf_min'] else "FAIL"
        lines.append(f"   Ratchet SF (CEM):     {sf2:.2f}  ->  {s2}")

    lines += [
        "",
        "4. FALL DETECTION RELIABILITY",
        f"   Required detection margin: >= {ANSI_LIMITS['clutch_margin_min']:.1f}x",
        "   (engagement speed / normal climbing speed)",
    ]
    if geom:
        margin = geom.clutch.safety_margin
        s = "PASS" if margin >= ANSI_LIMITS['clutch_margin_min'] else "FAIL"
        lines.append(f"   Clutch detection margin (CEM): {margin:.2f}x  ->  {s}")

    lines += [
        "",
        "5. TWO-FACTOR TAKE CONFIRMATION",
        "   Voice command + load cell confirmation required within 500ms",
        "   Verified by: state machine HIL tests and simulation",
        "",
        "OVERALL STATUS",
        "-"*40,
    ]

    if geom:
        all_cem = (
            geom.predicted_peak_force_kN * 1000 <= ANSI_LIMITS['peak_force_N'] and
            geom.predicted_arrest_distance_m * 1000 <= ANSI_LIMITS['arrest_distance_mm'] and
            geom.brake_drum.safety_factor >= ANSI_LIMITS['static_sf_min'] and
            geom.clutch.safety_margin >= ANSI_LIMITS['clutch_margin_min']
        )
        lines.append(f"   CEM analysis:       {'ALL PASS' if all_cem else 'FAILURES PRESENT'}")
    else:
        lines.append("   CEM analysis:       NOT RUN (open CEM Design tab and Regenerate)")

    if not drop_results:
        lines.append("   Physical testing:   PENDING - no drop test data available")
    elif all(r['data'].get('metrics', {}).get('ansi_all_pass', False) for r in drop_results):
        lines.append(f"   Physical testing:   ALL PASS ({len(drop_results)} drop test(s))")
    else:
        lines.append(f"   Physical testing:   FAILURES PRESENT - review drop test results")

    lines += [
        "",
        "NOTE: CEM results are computational predictions.",
        "Formal ANSI Z359.14 certification requires accredited lab testing.",
        "="*60,
    ]
    return "\n".join(lines)


